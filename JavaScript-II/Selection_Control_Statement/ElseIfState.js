

function Wish(value){
    if(value == "gm"){
        console.log("Good Morning Vishal")
    }else if(value === "ga"){
        console.log("Good Afternoon Vishal")
    }else if(value === "ge"){
        console.log("Good Evening Vishal")
    }else if(value === "gn"){
        console.log("Good Night Vishal")
    }else{
        console.log("There is nothing to wish")
    }
}

Wish("gam")